#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 

vector<ll> ss(vector<ll>&a)
{
	ll n=a.size();
	stack<ll>s;
	vector<ll>v;
	s.push(0);
	v.emplace_back(1);
	for(ll i=1;i<n;++i)
	{
		while(!s.empty()&&a[s.top()]<=a[i])
		{
			s.pop();
		}
		s.empty()?v.emplace_back(i+1):v.emplace_back(i-s.top());
		s.push(i);
	}
	return v;
}

