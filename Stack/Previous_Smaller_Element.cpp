using namespace std;
#define ll long long 
#define endl '\n' 
//Return's Previous Smaller Element/Element's Index 
vector<ll> ps(vector<ll>&a)
{
	ll n=a.size();
	stack<ll>s;
	vector<ll>v,u;
	s.push(0);
	v.emplace_back(-1);
	u.emplace_back(-1);
	for(ll i=1;i<n;++i)
	{
		while(!s.empty()&&a[s.top()]>=a[i])
		{
			s.pop();
		}
		s.empty()?u.emplace_back(-1):u.emplace_back(s.top());
		s.empty()?v.emplace_back(-1):v.emplace_back(a[s.top()]);
		s.push(i);
	}
	//if we need the Previous Smaller Element
	return v;
	//if we need the index of Previous Smaller Element
	//return u;
}
