#include<bits/stdc++.h>
using namespace std;

#define ll long long
#define endl '\n'\

const ll N=1e6+5;
bitset<N>p;
vector<ll>prime;

//Basic O(√n) Function (Brute Force)
map<ll,ll> basic_fac(ll n)
{
	map<ll,ll>h;//Stores the Prime Factor and it's count
	for(ll i=2;i*i<=n;++i)
	{
		while(n%i==0)
		{
			h[i]++;
			n/=i;
		}
	}
	//Corner Case
	if(n>1)
	{
		h[n]++;
		n=1;
	}
	return h;
}

//Using Sieve of Eratosthenes (Pre-computaion)
void sieve()
{
	//We are basically calculating all the primes under 1e6+5 Range 
	//Also can we calculated for 1e7 
	p.set();
	p[0]=p[1]=0;
	for(ll i=2;i*i<=N;++i)
	{
		if(p[i])
		{
			for(ll j=i*i;j<=N;j+=i)
			{
				if(p[j])
					p[j]=0;
			}
		}
	}
	for(ll i=2;i<=N;++i)
	{
		if(p[i])
		{
			prime.emplace_back(i);
		}
	}
}

//Factorization Function (O logn)  (After pre computation of prime nos)
map<ll,ll>sieve_fac(ll n)
{
	map<ll,ll>m;//Stores the Prime Factor and it's count
	//Using Prime Numbers to check for Factors
	for(ll i=0;prime[i]*prime[i]<=n;++i)
	{
		while(n%prime[i]==0)
		{
			m[prime[i]]++;
			n/=prime[i];
		}
	}
	//Corner Case
	if(n>1)
	{
		m[n]++;
		n=1;
	}
	return m;
}

int main()
{
	ll t=1;
	cin>>t;
	sieve();
	while(t--)
	{
		ll n;
		cin>>n;
		map<ll,ll>m=sieve_fac(n); //OR basic_fac(n);
		for(auto &i:m)
		{
			cout<<i.first<<" "<<i.second<<endl;
		}
		cout<<"----"<<endl;
	}
	return 0;
}