using namespace std;
#define ll long long 
#define endl '\n' 

const ll N=1e7+5;

bitset<N>p;//Using bitset

//Sieve of Eratosthenes
void sieve()
{
	p.set();//sets all the elements as 1
	vector<ll>prime;
	p[1]=p[0]=0;
	for(ll i=2;i*i<=N;i++)
	{
		if(p[i])//if true check for it multiples
		{
			for(ll j=i*i;j<=N;j+=i)
			{
				if(p[j])//if true set fasle
				{
					p[j]=0;
				}
			}
		}
	}
	ll c=0;//count of prime nos
	for(ll i=2;i<N;++i)
	{
		if(p[i])
		{
			c++;
			cout<<i<<" ";
			prime.emplace_back(i);//storing prime numbers
		}
	}
	//cout<<c<<endl;
	//return prime;
}
