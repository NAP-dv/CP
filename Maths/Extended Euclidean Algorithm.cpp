using namespace std;
#define ll long long 
#define endl '\n' 

/*
	ax+by=gcd(a,b)
	ax1 + by1 = gcd(a,b)
	ax2 + by2 = gcd(b,a%b)
	=> x1 = y2 AND y1 = x2 - [a/b] * y2
*/
pair<ll,ll> egcd(ll a,ll b)
{
	if(b==0)
	{
		return make_pair(1,0);
	}
	pair<ll,ll> t=egcd(b,a%b);
	return make_pair(t.second,t.first-(a/b)*t.second);
}

