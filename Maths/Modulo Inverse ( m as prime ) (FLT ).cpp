#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
//Iterative Function.
ll powm(ll x,ll y,ll m)
{
	ll ans=1;
	ll r=1;
	/*
	Basically we r checking the set bit.
	eg:-  
	 x^7 = x^1 * x^2 ^ x^4 
	So, if we look at the powers.
	 RHS:- 
	  sum of powers = 1 + 2 + 4  = 7 == LHS
	This is nothing just the representation of x^7 (LHS) in binary form.
	*/
	while(r<=y)
	{
		if(r&y)
		{
			ans=(ans*x)%m;
		}
		x=(x*x)%m;
		r<<=1;
	}
	return ans;
}
//Recursive Function.
ll powmr(ll x,ll y,ll m)
{
	if(y==0) return 1;
	//if y%2==0 (even) then x^y = x^(y/2) * x^(y/2) OR (x^(y/2))^2.
	if(y%2==0)
	{
		ll t=powmr(x,(y/2),m);
		return (t*t)%m;
	}
	//if y%2==1 (odd) then x^y =  ( x^(y/2) *x^(y/2) ) * x OR  ((x^(y/2))^2) * x.
	else
	{
		ll t=powmr(x,(y/2),m);
		return ((((t*t)%m)*x)%m);
	}
}
//Modulo Inverse Function (M is prime)
//Fermat's Little theorem
// x & m are co-prime.
// (x^y)% m = (x^(y%φ(m)))%m -----> φ(m) is nothing just count of positive no's that are less than m and are co-prime to m.
// φ(m) for prime number(s) = m-1 
// => (x^y)%m = (x^(y%φ(m)))%m = (x^(y%(m-1)))%m 
// => (x^(-1))%m (m is prime) = (x^(-1)%φ(m))%m
// => (x^(-1)%φ(m))%m = (x^(-1+φ(m))%φ(m))%m
// => φ(m) for prime m = m-1 => -1+φ(m) == -1+m-1 == m-2 
// Therefore, (φ(m)-1)%φ(m) = (m-2)%(m-1)   <--> Replacing φ(m) by m-1
// As m-2 < m-1 , so (m-2) % (m-1) == m-2
// Hence (x^y)%m =(x^(m-2))%m 
ll powi(ll x,ll m)
{
	return powm(x,m-2,m);
}
int main()
{
	ll t=1;
	cin>>t;
	while(t--)
	{
		ll x,y,m;
		cin>>x>>y>>m;
		cout<<powi(x,y);
	}
}
