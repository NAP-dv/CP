#include<bits/stdc++.h>
using namespace std;

#define ll long long
#define endl '\n'\

const ll N=1e6+5;
vector<ll>prime(N,0);

//Basic Sieve
void sieve()
{
	for(ll i=2;i<=N;++i)
	{
		if(!prime[i])
		{
			for(ll j=i;j<=N;j+=i)
			{
				prime[j]++;
			}
		}
	}
}
int main()
{
	ll t=1;
	//cin>>t;
	while(t--)
	{
		sieve();
		for(ll i=0;i<prime.size();++i)
		{
			cout<<prime[i]<<" ";
		}
	}
	return 0;
}