#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 

const ll N=1e6+5;
vector<ll>p(N);
//ϕ(ab)=ϕ(a)⋅ϕ(b)        	--> If a & b  are prime
//ϕ(ab)=ϕ(a)⋅ϕ(b)⋅(d/ϕ(d))  	--> d is the gcd of a & b


//For prime ϕ(p)= p-1
//For composite ϕ(n)= n * (1-1/p1) * (1-1/p2) * .... * (1-1/pk)

//O(√n) Algorithm (Optimized brute force)
ll brut_euler(ll n)
{
	ll ans=n;
	for(ll i=2;i*i<=n;++i) //Same as we do in Factorization by brute force
	{
		if(n%i==0)
		{
			while(n%i==0)
			{
				n/=i;
			}
			ans=(ans*(i-1))/i;  //OR ans-=ans/i;
		}
	}
	if(n>1)
	{
		ans=(ans*(n-1))/n;  //OR ans-=ans/n;
		n=1;
	}
}


//O(nloglogn) (Using Sieve)
void euler_tot() ///
{
	p[0]=0;
	p[1]=1;
	for(ll i=2;i<=N;++i) 
	{
		p[i]=i;
	}
	for(ll i=2;i*i<=N;++i) //Important ! Not i*i<=N !  ---  As we will miss the prime nos > √n
	{
		if(p[i]==i)
		{
			p[i]-=1;
			for(ll j=i*2;j<=N;j+=i)
			{
				p[j]-=p[j]/i;
			}
		}
	}
}

int main()
{
	ll t=1;
	//cin>>t;
	while(t--)
	{
		ll n;
		cin>>n;
		euler_tot();
		for(ll i=0;i<N;++i)
		{
			cout<<i<<" "<<p[i]<<endl;
		}
	}
	return 0;
}